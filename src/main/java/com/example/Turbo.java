package com.example;

public class Turbo {

    public void activateHighPressure() {
        System.out.println("Turbo has a pressure of 2.0 bar!");
    }

    public void activateMediumPressure() {
        System.out.println("Turbo has a pressure of 1.2 bar!");
    }

    public void activateLowPressure() {
        System.out.println("Turbo has a pressure of 0.5 bar!");
    }
}